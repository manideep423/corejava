package training;

public class Switch {
	public static void main (String[] args) {
		char grade='A';
		switch(grade){
		case 'A':
		System.out.println("ALL");
		break;
		case 'B':
		System.out.println("is");
		break;
		case 'C':
		System.out.println("well");
		break;
		default:
			System.out.println("great");
		}

	}

}
